package com.ghqkl.schedule.model;

public class MiningConfigBean {
	private Integer id;
	
	private Double numStart;
	
	private Double numEnd;
	
	private Integer spoceNum;
	
	private Integer lastNum;
	
	private String miningOpen;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Double getNumStart() {
		return numStart;
	}

	public void setNumStart(Double numStart) {
		this.numStart = numStart;
	}

	public Double getNumEnd() {
		return numEnd;
	}

	public void setNumEnd(Double numEnd) {
		this.numEnd = numEnd;
	}

	public Integer getSpoceNum() {
		return spoceNum;
	}

	public void setSpoceNum(Integer spoceNum) {
		this.spoceNum = spoceNum;
	}

	public Integer getLastNum() {
		return lastNum;
	}

	public void setLastNum(Integer lastNum) {
		this.lastNum = lastNum;
	}

	public String getMiningOpen() {
		return miningOpen;
	}

	public void setMiningOpen(String miningOpen) {
		this.miningOpen = miningOpen;
	}
	
	
	
}
